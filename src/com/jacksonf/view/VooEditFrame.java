    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            voo.setAviao((Aviao)cbxAviao.getSelectedItem());
            voo.setOrigem(txtOrigem.getText());
            voo.setDestino(txtDestino.getText());
            
            String testDate = txtHora.getText();
            
            Date date = formatter.parse(testDate);
            
            voo.setHora(new Date());

            if (voo.getId() > 0) {
                vooRN.alterar(voo);
            } else {
                vooRN.salvar(voo);
            }
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Ocorreu um erro", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

