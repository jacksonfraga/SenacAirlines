    private void EditarRegistro() {
        Aviao aviaoSelec = listagem.get(jTable1.getSelectedRow());

        AviaoEditFrame frame = new AviaoEditFrame(aviaoSelec);
        frame.setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);
        frame.setVisible(true);
    }
    
    private void ExcluirRegistro(){
        
        //default icon, custom title
        int n = JOptionPane.showConfirmDialog(
            this,
            "Deseja excluir o registro?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION);
    
        if (n == JOptionPane.YES_OPTION) {
            
            try {
                Aviao aviaoSelec = listagem.get(jTable1.getSelectedRow());
                aviaoRN.excluir(aviaoSelec);
                
                CarregarRegistros();
                JOptionPane.showMessageDialog(this, "Registro exclu√≠do com sucesso!", "Info", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }            
            
        }        
            
        
    }

